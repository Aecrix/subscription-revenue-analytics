CREATE OR ALTER VIEW vw_monthly_revenue AS
SELECT
    fp.payment_month,
    SUM(fp.amount) AS monthly_revenue,
    COUNT(fp.payment_id) AS total_payments,
    COUNT(DISTINCT fs.user_id) AS active_customers,
    ROUND(SUM(fp.amount) / COUNT(DISTINCT fs.user_id), 2) AS arpu
FROM fact_payments AS fp
JOIN fact_subscriptions AS fs
    ON fp.subscription_id = fs.subscription_id
WHERE fp.payment_status = 'Paid'
GROUP BY fp.payment_month;
GO

