-- 7. Active Customers
SELECT
    COUNT(DISTINCT user_id) AS active_customers
FROM fact_subscriptions
WHERE subscription_status = 'Active';

-- Measures the current paying customer base.
-- Uses COUNT(DISTINCT) to avoid double-counting customers.


-- 8. Churn Rate
SELECT
    ROUND(
        100.0 * SUM(CASE WHEN subscription_status = 'Cancelled' THEN 1 ELSE 0 END)
        / COUNT(*),
        2
    ) AS churn_rate_pct
FROM fact_subscriptions;

-- Measures the percentage of customers who have churned.
-- Uses conditional aggregation to calculate the churn percentage.


-- 9. Customer Retention Rate
SELECT
    ROUND(
        100.0 * SUM(CASE WHEN subscription_status = 'Active' THEN 1 ELSE 0 END)
        / COUNT(*),
        2
    ) AS retention_rate_pct
FROM fact_subscriptions;

-- Measures the percentage of customers that remain active.
-- Complements churn rate and is commonly tracked by SaaS companies.


-- 10. Average Revenue Per User (ARPU)
SELECT
    ROUND(
        SUM(fp.amount) / COUNT(DISTINCT fs.user_id),
        2
    ) AS arpu
FROM fact_payments AS fp
JOIN fact_subscriptions AS fs
    ON fp.subscription_id = fs.subscription_id
WHERE fp.payment_status = 'Paid';

-- Measures the average revenue generated per customer.
-- Calculates revenue using successful payments and unique customers.

-- 11. Customer Lifetime Value (LTV)
SELECT
    ROUND(
        (SUM(fp.amount) / COUNT(DISTINCT fs.user_id))
        *
        (AVG(fs.subscription_length_days) / 30.0),
        2
    ) AS estimated_ltv
FROM fact_payments AS fp
JOIN fact_subscriptions AS fs
    ON fp.subscription_id = fs.subscription_id
WHERE fp.payment_status = 'Paid';

-- Estimates the lifetime revenue generated by an average customer.
-- Uses a simplified LTV formula suitable for portfolio projects.


-- 12. Refund Rate
SELECT
    ROUND(
        COUNT(CASE WHEN refund_flag = 1 THEN 1 END) * 100.0
        / COUNT(*),
        2
    ) AS refund_rate_pct
FROM fact_payments;

-- Measures the percentage of payments that resulted in refunds.
-- Uses conditional aggregation to identify refunded transactions.


-- 13. Monthly Revenue Trend
WITH monthly_revenue AS
(
    SELECT
        payment_month,
        SUM(amount) AS revenue
    FROM fact_payments
    WHERE payment_status = 'Paid'
    GROUP BY payment_month
)

SELECT
    payment_month,
    revenue,
    LAG(revenue) OVER (ORDER BY payment_month) AS previous_month_revenue,
    revenue - LAG(revenue) OVER (ORDER BY payment_month) AS revenue_change
FROM monthly_revenue
ORDER BY payment_month;

-- Compares each month's revenue with the previous month.
-- Uses LAG() to perform month-over-month analysis without self-joins.


-- 14. Monthly Revenue Growth Percentage
WITH monthly_revenue AS
(
    SELECT
        payment_month,
        SUM(amount) AS revenue
    FROM fact_payments
    WHERE payment_status = 'Paid'
    GROUP BY payment_month
)

SELECT
    payment_month,
    revenue,
    ROUND(
        (
            (revenue - LAG(revenue) OVER (ORDER BY payment_month))
            * 100.0
        )
        /
        NULLIF(LAG(revenue) OVER (ORDER BY payment_month), 0),
        2
    ) AS revenue_growth_pct
FROM monthly_revenue
ORDER BY payment_month;

-- Measures month-over-month revenue growth.
-- Uses NULLIF() to safely avoid division-by-zero errors.


