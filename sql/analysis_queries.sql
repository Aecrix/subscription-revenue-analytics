-- 1. Monthly Recurring Revenue (MRR)
SELECT
    payment_month,
    SUM(amount) AS monthly_recurring_revenue
FROM fact_payments
WHERE payment_status = 'Paid'
GROUP BY payment_month
ORDER BY payment_month;

--This query calculates the total successful subscription revenue collected each month.
--It excludes failed or refunded payments so that only realized recurring revenue contributes to MRR.


--2. Annual Recurring Revenue (ARR)
SELECT
    payment_month,
    SUM(amount) AS mrr,
    SUM(amount) * 12 AS arr
FROM fact_payments
WHERE payment_status = 'Paid'
GROUP BY payment_month
ORDER BY payment_month;

--ARR is typically derived as MRR × 12 for subscription businesses.


--3. Monthly Revenue Summary
SELECT
    payment_month,
    COUNT(*) AS successful_transactions,
    SUM(amount) AS revenue,
    AVG(amount) AS avg_transaction_value
FROM fact_payments
WHERE payment_status = 'Paid'
GROUP BY payment_month
ORDER BY payment_month;

--Observe overall revenue movement month over month.
--Finance teams use this to identify seasonality and unusual revenue changes.


-- 4. Revenue by Plan
SELECT
    dp.plan_name,
    dp.billing_cycle,
    SUM(fp.amount) AS total_revenue,
    COUNT(fp.payment_id) AS total_payments
FROM fact_payments AS fp
JOIN fact_subscriptions AS fs
    ON fp.subscription_id = fs.subscription_id
JOIN dim_plans AS dp
    ON fs.plan_id = dp.plan_id
WHERE fp.payment_status = 'Paid'
GROUP BY
    dp.plan_name,
    dp.billing_cycle
ORDER BY total_revenue DESC;

--Determine which subscription plans generate the most revenue.
--Helps pricing and product teams understand plan performance.


--5. Revenue by Country
