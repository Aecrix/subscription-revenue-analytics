-- 18. Customer Ranking Comparison
WITH customer_revenue AS
(
    SELECT
        du.customer_id,
        du.country,
        SUM(fp.amount) AS total_revenue
    FROM fact_payments AS fp
    JOIN fact_subscriptions AS fs
        ON fp.subscription_id = fs.subscription_id
    JOIN dim_users AS du
        ON fs.user_id = du.user_id
    WHERE fp.payment_status = 'Paid'
    GROUP BY
        du.customer_id,
        du.country
)

SELECT
    customer_id,
    country,
    total_revenue,

    ROW_NUMBER() OVER (
        ORDER BY total_revenue DESC
    ) AS row_number,

    RANK() OVER (
        ORDER BY total_revenue DESC
    ) AS rank_number,

    DENSE_RANK() OVER (
        ORDER BY total_revenue DESC
    ) AS dense_rank
FROM customer_revenue
ORDER BY total_revenue DESC;

-- Compares the behavior of ROW_NUMBER(), RANK(), and DENSE_RANK().
-- Demonstrates how SQL handles ranking when multiple rows have the same value.

-- 19. Monthly Cohort Retention Analysis
WITH cohort AS
(
    SELECT
        user_id,
        DATEFROMPARTS(signup_year, signup_month, 1) AS cohort_month
    FROM dim_users
),

cohort_size AS
(
    SELECT
        cohort_month,
        COUNT(*) AS cohort_size
    FROM cohort
    GROUP BY cohort_month
),

customer_activity AS
(
    SELECT DISTINCT
        fs.user_id,
        c.cohort_month,
        DATEFROMPARTS(
            YEAR(fp.payment_date),
            MONTH(fp.payment_date),
            1
        ) AS activity_month
    FROM fact_payments AS fp
    JOIN fact_subscriptions AS fs
        ON fp.subscription_id = fs.subscription_id
    JOIN cohort AS c
        ON fs.user_id = c.user_id
    WHERE fp.payment_status = 'Paid'
),

retention AS
(
    SELECT
        cohort_month,
        activity_month,
        COUNT(DISTINCT user_id) AS active_customers
    FROM customer_activity
    GROUP BY
        cohort_month,
        activity_month
)

SELECT
    r.cohort_month,
    r.activity_month,
    cs.cohort_size,
    r.active_customers,
    ROUND(
        100.0 * r.active_customers / cs.cohort_size,
        2
    ) AS retention_pct
FROM retention AS r
JOIN cohort_size AS cs
    ON r.cohort_month = cs.cohort_month
ORDER BY
    r.cohort_month,
    r.activity_month;

-- Shows how well each signup cohort is retained over time.
-- Demonstrates CTEs, date manipulation, and multi-stage analytical SQL.


